    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/csia?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "riyamani@3002");
            String sql = "select * from teachers_increment_data where Teacher_name = ? and Year = ? and Id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, data_name.getText());
            pstmt.setInt(2, Integer.parseInt(data_year.getText()));
            pstmt.setInt(3, Integer.parseInt(id.getText()));

            ResultSet rs = pstmt.executeQuery();
            if(rs.next()){
                data_name.setText(rs.getString("Teacher_name"));
                data_year.setText(rs.getString("Year"));
                data_current_salary.setText(rs.getString("Current_salary"));
                if(rs.getString("Attendance").equals("Yes")){
                    attendance_yes.doClick();
                }
                else if(rs.getString("Attendance").equals("No")){
                    attendance_no.doClick();
                }
                else if(rs.getString("Attendance").equals("Full Attendance")){
                    Attendance_full.doClick();
                }
                data_awards.setText(rs.getString("Awards"));
                award_address.setText(rs.getString("File_awards_evidence"));
                if(rs.getString("Academic_duties").equals("Yes")){
                    duties_yes.doClick();
                }
                else{
                    duties_no.doClick();
                }
                if(rs.getString("Upgraded_Diploma").equals("Diploma")){
                    diploma.doClick();
                }
                diploma_address.setText(rs.getString("File_diploma_evidence"));
                if(rs.getString("Upgraded_Degree").equals("Degree")){
                    degree.doClick();
                }
                degree_address.setText(rs.getString("File_degree_evidence"));
                data_pass.setText(rs.getString("Pass_percentage"));
                data_tc.setText(rs.getString("Number_of_TCs_issued"));
                output.setText(rs.getString("Salary"));
                raise.setText(rs.getString("Raise"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Record not found. Note: Please type the name of the teacher and the year and ID to search for the required record.");
            }
            conn.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Record not found. Note: Please type the name of the teacher and the year and ID to search for the required record.");
        }
    }//GEN-LAST:event_searchActionPerformed

