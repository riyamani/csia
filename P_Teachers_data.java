    private void edit_raiseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_raiseActionPerformed

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/csia?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "riyamani@3002");
            String sql = "update teachers_increment_data set Raise = ?, Salary = ? where Id = ? and Teacher_name = ? and Year = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, Integer.parseInt(raise.getText()));
            int out = Integer.parseInt(output.getText());
            int rai = Integer.parseInt(raise.getText());
            int outt = out + rai;
            pstmt.setInt(2, outt);
            pstmt.setString(3, id.getText());
            pstmt.setString(4, data_name.getText());
            pstmt.setInt(5, Integer.parseInt(data_year.getText()));

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Record updated successfully.");
            
        conn.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please make sure you add no decimal values or ',' in the 'raise' value.");
        }
        new Teachers_list_1().setVisible(true); //To show the user whether the changes are updated in the database through the table.
        dispose();
    }                                      

